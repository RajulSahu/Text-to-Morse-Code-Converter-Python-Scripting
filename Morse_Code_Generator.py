def encryptor(text):
    encrypted = ""
    for letter in text:
        if letter != " ":
            encrypted = encrypted + MORSE_CODE_DICT.get(letter) + " "
        else:
            encrypted = encrypted + " "
    print(encrypted)


print("\n\n\t\tThe Morse Code Generator!")
text_to_encrypt = input("Enter the text to Encrypt : ").upper()
encryptor(text_to_encrypt)
